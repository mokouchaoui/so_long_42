#include "libft.h"
#include <stdio.h>

char	*ft_strrchr(const char *s, int c)
{	
	int	i;

	i = ft_strlen(s);
	while (i >= 0)
	{
		if (s[i] == (char)c)
			return ((char *)s + i);
		if (*s == '\0')
			return (NULL);
		i--;
	}
	return (NULL);
}
